public class Variables {

    public static void main(String[] args) {

        System.out.println("Toplam: " + sayilar(4, 7));
        String name = "Veli";
        int number = 987845456;
        int age = 37;
        float size = 1.82f;


        System.out.println("Name: " + name);
        System.out.println("Number: " + number);
        System.out.println("Age: " + age);
        System.out.println("Size: " + size);

    }

    public static int sayilar(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }
}
// Main'den metot çağrılma işlemleri yapılmıştır.

